require 'active_record'
require 'pry'

class Single_number < ActiveRecord::Base
	def isPrime(nums)
		prime_list = []
		num = (1..100)
		prime_num = [2, 3, 5, 7]
		num.each do |x|
		unless x%2 == 0 || x%3 == 0 || x%5 == 0 || x%7 == 0 then
			prime_num << x
		end
		prime_num.each do |x|
			prime_list << nums.where(number: x)
		end
		return prime_list
	end
	end
end

